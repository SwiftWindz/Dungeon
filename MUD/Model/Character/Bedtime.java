package MUD.Model.Character;

/**
 * An enumeration of the different types of "bedtimes" an NPC can have.
 * An NPC's bedtime determines how it will be buffed/debuffed when
 * the day/night cycle changes.
 * @author Phil Ganem
 */
public enum Bedtime {
        
        Nocturnal,
        Diurnal
    
}
